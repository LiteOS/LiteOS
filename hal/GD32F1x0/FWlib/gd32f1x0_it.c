/*!
    \brief      this function handles external lines 4 to 15 interrupt request
    \param[in]  none
    \param[out] none
    \retval     none
*/
void EXTI4_15_IRQHandler(void)
{
    if(RESET != exti_interrupt_flag_get(EXTI_13)){
        gd_eval_ledtoggle(LED1);
    }
    exti_interrupt_flag_clear(EXTI_13);
}
